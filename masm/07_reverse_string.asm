ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	MSG1 DB 0AH,0DH,"ENTER THE STRING: $"
	MSG2 DB 0AH,0DH,"THE REVERSE :$"
	STR1 DB 20 DUP("?")
	REV DB 20 DUP("?")
DATA ENDS
DISPLAY MACRO MSG
	MOV AH,09H
	LEA DX,MSG
	INT 21H
ENDM
CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	DISPLAY MSG1
	MOV CX,0000H
	LEA SI,STR1
LOOP1:	MOV AH,01H
	INT 21H

	MOV [SI],AL
	CMP AL,0DH
	JE LOOP2
	INC SI
	INC CX
	JMP LOOP1
LOOP2:	MOV BL,"$"
	MOV [SI],BL
	MOV DI,SI
	LEA SI,REV
	DEC DI
LOOP3:	MOV BL,[DI]
	MOV [SI],BL
	DEC DI
	INC SI
	DEC CX
	JNZ LOOP3
	MOV BL,"$"
	MOV [SI],BL
	DISPLAY MSG2
	DISPLAY REV
	MOV AH,4CH
	INT 21H
CODE ENDS
END START

